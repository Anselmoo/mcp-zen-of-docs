# ---------------------------------------------------------------------------
# Deployment URL configuration with constructive validation
# ---------------------------------------------------------------------------

_PLACEHOLDER_DOMAINS = frozenset(
    {
        "example.com",
        "dev.example.com",
        "staging.example.com",
        "localhost",
    }
)


class DeploymentUrlConfig(ModelBase):
    """Project-specific deployment environment URLs.

    Validators reject placeholder domains (``example.com``, ``localhost``) and
    return a ``PydanticCustomError`` with *constructive* guidance that tells the
    caller exactly what is expected.
    """

    model_config = ConfigDict(extra="forbid", str_strip_whitespace=True, frozen=True)

    dev_url: AnyHttpUrl | None = Field(
        default=None,
        description=(
            "Development environment URL (e.g. https://dev.myproject.io). "
            "Leave empty to be prompted during onboarding."
        ),
    )
    staging_url: AnyHttpUrl | None = Field(
        default=None,
        description=(
            "Staging environment URL (e.g. https://staging.myproject.io). "
            "Leave empty to be prompted during onboarding."
        ),
    )
    production_url: AnyHttpUrl | None = Field(
        default=None,
        description=(
            "Production environment URL (e.g. https://myproject.io). "
            "Leave empty to be prompted during onboarding."
        ),
    )

    @field_validator("dev_url", "staging_url", "production_url", mode="after")
    @classmethod
    def reject_placeholder_urls(cls, v: AnyHttpUrl | None) -> AnyHttpUrl | None:
        """Reject ``example.com`` and other placeholder domains with constructive feedback."""
        if v is None:
            return v
        host = str(v.host or "").lower()
        if host in _PLACEHOLDER_DOMAINS:
            _err_type = "placeholder_url_rejected"
            _err_msg = (
                "'{url}' looks like a placeholder. "
                "Please provide your real deployment URL "
                "(e.g. https://docs.your-project.io). "
                "Leave the field empty/null if the URL is not yet known "
                "— the onboarding tool will prompt you interactively."
            )
            raise PydanticCustomError(_err_type, _err_msg, {"url": str(v)})
        return v


