class ModelBase(BaseModel):
    """Shared base model with strict contracts."""

    model_config = ConfigDict(extra="forbid", str_strip_whitespace=True, frozen=True)


class PipelineContext(ModelBase):
    """Shared context passed between pipeline tools for chaining.

    Each tool in the pipeline (detect → plan → scaffold → compose → enrich →
    validate → score) populates relevant fields so the next tool can
    auto-configure without requiring the user to re-specify parameters.
    """

    framework: FrameworkName | None = Field(
        default=None, description="Detected or specified documentation framework."
    )
    project_root: Path | None = Field(
        default=None, description="Root path of the project being documented."
    )
    docs_root: Path | None = Field(
        default=None, description="Root path of the documentation directory."
    )
    doc_paths: list[str] = Field(
        default_factory=list,
        description="Paths to documentation files created or processed by previous tools.",
    )
    last_tool: str | None = Field(
        default=None, description="Name of the last tool that produced this context."
    )


